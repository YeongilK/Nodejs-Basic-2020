for (let i = 5; i > 0; i--) {
    let stars = '';
    for (let k = 0; k < 5 - i; k++) {
        stars += ' ';
    }
    for (let k = 0; k < i; k++) {
        stars += '*';
    }
    console.log(stars);
}

